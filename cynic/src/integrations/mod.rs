//! Provides Cynic implementations for some external types

#[cfg(feature = "chrono")]
/// Cynic support for [chrono](https://github.com/chronotope/chrono) types.
pub mod chrono;

#[cfg(feature = "bson")]
/// Cynic support for [bson](https://github.com/mongodb/bson-rust) types.
pub mod bson;

#[cfg(feature = "uuid")]
/// Cynic support for [uuid](https://doc.rust-lang.org/uuid/uuid/struct.Uuid.html) types.
pub mod uuid;

#[cfg(feature = "url")]
/// Cynic support for [url](https://github.com/servo/rust-url) types.
pub mod url;
